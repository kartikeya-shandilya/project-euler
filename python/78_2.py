
m=100000

p=[1]
for i in xrange(1,m+1):
	j=1; k=1; s=0
	while j>0:
		j=i-(3*k**2+k)//2
		if j>=0:	s-=(-1)**k*p[j]
		j=i-(3*k**2-k)//2
		if j>=0:	s-=(-1)**k*p[j]
		k+=1
	p.append(s)

